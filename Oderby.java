
//Order by
import java.util.*;

public class Oderby {

    public static void main(String args[]) {
        HashSet<Integer> set = new HashSet<>();
        set.add(1);
        set.add(5);
        set.add(2);
        set.add(3);
        System.out.println(set);
    }
}

// output:- 1, 2, 3, 5
